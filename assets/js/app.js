let company = document.getElementById("company");
let brand = document.getElementById("brand");
let allCars = document.getElementById("allCars");

company.innerHTML = `<option value="all">All</option>`;
brand.innerHTML = `<option value="">Select model</option>`;
allCars.innerHTML = "";

for (var key in cars) {
  company.innerHTML += `<option value="${key}">${key.toUpperCase()}</option>`;
}

function companyChange() {
  brand.innerHTML = `<option value="">Select model</option>`;
  allCars.innerHTML = "";

  var selectedCompany = company.value;
  if (selectedCompany === "all") {
    for (var key in cars) {
      for (var key1 in cars[key]) {
        for (var key2 in cars[key][key1]) {
          displayCarData(cars[key][key1][key2]);
        }
      }
    }
  } else {
    for (var key in cars[selectedCompany]) {
      brand.innerHTML += `<option value="${key}">${key.toUpperCase()}</option>`;
    }
  }
}

function filterCars() {
  var selectedCompany = company.value;
  var selectedModel = brand.value;
  allCars.innerHTML = "";

  if (selectedCompany === "all") {
    for (var key in cars) {
      for (var key1 in cars[key]) {
        for (var key2 in cars[key][key1]) {
          displayCarData(cars[key][key1][key2]);
        }
      }
    }
  } else {
    var carData = cars[selectedCompany][selectedModel];
    if (carData) {
      for (var i in carData) {
        displayCarData(carData[i]);
      }
    } else {
      allCars.innerText = "please select car model";
    }
  }
}

function displayCarData(allCar) {
  allCars.innerHTML += `
  <div class="col-md-4 mb-4">
  <div class="card animate__animated animate__zoomIn" style="animation-duration: 1s;">
    <div class="card-body">
      <div class="img_holder"><img class="card-img-top" src="${
        allCar.src
      }" alt="Car image"></div>
      <h1 class="card-title">${allCar.name} <span class="title_year">${
    allCar.year
  }</span></h1>
      <h6>${allCar.gears}</h6>
      <h3>${allCar.Price}</h3>
      <div class="color-holder">
        ${displayColors(allCar.Colors)}
      </div>
    </div>
  </div>
</div>`;

  function displayColors(colors) {
    let colorSpans = "";
    colors.forEach((coloritem) => {
      colorSpans += `<span class="color_sp" style="background-color: ${coloritem};"></span>`;
    });
    return colorSpans;
  }
}
